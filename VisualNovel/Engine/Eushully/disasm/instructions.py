# Build lookup tables for fast access
_OPCODE_TO_DEF = {d.op_code: d for d in INSTRUCTION_DEFINITIONS}
_LABEL_TO_DEF = {d.label: d for d in INSTRUCTION_DEFINITIONS}


def instruction_for_opcode(op_code: int, offset: int = 0):
    def_obj = _OPCODE_TO_DEF.get(op_code)
    if def_obj is None:
        raise ValueError(f"Unknown instruction: 0x{op_code:x} at offset 0x{offset:x}")
    return def_obj


def instruction_for_label(label: str):
    def_obj = _LABEL_TO_DEF.get(label)
    if def_obj is None:
        raise ValueError(f"Unknown instruction: {label}")
    return def_obj
